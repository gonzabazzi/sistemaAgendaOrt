    private void login() {
        String nom = tfNombre.getText();
        String pass = new String(tfPassword.getPassword());
        Object obj = this.llamarLogin(nom,pass);
        if(obj==null){
            JOptionPane.showMessageDialog(this, "Login incorrecto");
        }else{
            setVisible(false);
            this.proximoCasoUso(obj);
        }
       
    }

